height = int(input("How tall are you in cm: "))

if height > 140 : 
  print(f"You are tall enough to ride the rollercoaster")

elif height < 140 and height > 0: 
  print(f"You are not tall enough for the rollercoaster")

elif height < 0 : 
  print(f"Invalid height!")

