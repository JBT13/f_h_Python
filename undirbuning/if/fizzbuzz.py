number = int(input("Enter in a number: "))

if number % 3 == 0  and number % 2 == 0  : 
  print ("FizzBuzz")

elif number % 3  == 0: 
  print("Buzz")

elif number % 2 == 0 :
  print("Fizz")

else: 
  print(number)

# ef talan gengur upp í 2 kemur Fizz
# ef talan gengur upp í 3 kemur Buzz
