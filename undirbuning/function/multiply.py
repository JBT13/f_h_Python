def multiply(a, b):
  a = int(input("Enter in a number: "))
  b = int(input("Enter in a second number: "))
  return a * b

def power(a, b):
  a = int(input("Enter in a number: "))
  b = int(input("Enter in a second number: "))
  return a ** b

print("Multiplication result:", multiply(0, 0))
print("Power result:", power(0, 0))