n = int(input()) # input

sum = 0 # the sum variable that we gonna put more numbers into

while n != 10:    # while our input is not 10 it will run

    sum += n   # we rewrite our variable and we + with each n input as long as it is not 10

    n = int(input()) # we recall out input for more numbers 

print(sum) # we print our sum 

